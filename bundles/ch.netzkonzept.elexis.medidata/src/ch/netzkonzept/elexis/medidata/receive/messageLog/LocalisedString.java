package ch.netzkonzept.elexis.medidata.receive.messageLog;

public class LocalisedString {
	private String de;
	private String fr;
	private String it;

	public String getDe() {
		return de;
	}

	public void setDe(String de) {
		this.de = de;
	}

	public String getFr() {
		return fr;
	}

	public void setFr(String fr) {
		this.fr = fr;
	}

	public String getIt() {
		return it;
	}

	public void setIt(String it) {
		this.it = it;
	}
}
